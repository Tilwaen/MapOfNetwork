     private void editLabel() {
        //String labelNumber = "labelSpot" + spot;
        JLabel label = Main.getLabels().get(spot-1);
        label.setText("");
        
        Icon image = null;
        String deviceTypeString = deviceType.toString().toUpperCase();
        switch(deviceTypeString) {
            case "COMPUTER" : 
                image = new ImageIcon(getClass().getClassLoader().getResource(computerPath));
                break;
            case "HUB" :
                image = new ImageIcon(getClass().getClassLoader().getResource(hubPath));
                break;
            case "MODEM" :
                image = new ImageIcon(getClass().getClassLoader().getResource(modemPath));
                break;
            case "ROUTER" :
                image = new ImageIcon(getClass().getClassLoader().getResource(routerPath));
                break;
            case "SWITCH12" :
                image = new ImageIcon(getClass().getClassLoader().getResource(switch12Path));
                break;
            case "SWITCH24" :
                image = new ImageIcon(getClass().getClassLoader().getResource(switch24Path));
                break;
            case "SWITCH48" :
                image = new ImageIcon(getClass().getClassLoader().getResource(switch48Path));
                break;
        }
        label.setIcon(image);
        label.setHorizontalTextPosition( SwingConstants.CENTER );
        label.setToolTipText( name + ", number of ports: " + numberOfPorts + ", address: " + address );
    }
    
    private Device parseTextFields() {
        name = textFieldName.getText();
        int deviceTypeIndex = comboBoxDeviceType.getSelectedIndex();
        address = textFieldAddress.getText();
        String numberOfPortsString = textFieldNumberOfPorts.getText();
        String spotString = textFieldSpot.getText();
        
        deviceType = DeviceType.COMPUTER;
        switch( deviceTypeIndex ) {
            case 0:
                deviceType = DeviceType.COMPUTER;
                break;
            case 1:
                deviceType = DeviceType.HUB;
                break;
            case 2:
                deviceType = DeviceType.MODEM;
                break;
            case 3:
                deviceType = DeviceType.ROUTER;
                break;
            case 4:
                deviceType = DeviceType.SWITCH12;
                break;
            case 5:
                deviceType = DeviceType.SWITCH24;
                break;
            case 6:
                deviceType = DeviceType.SWITCH48;
                break;
        }
        
        numberOfPorts = 0;
        try {
            numberOfPorts = Integer.parseInt(numberOfPortsString);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Enter valid number.");
            return null;
        }
        
        spot = 0;
        try {
            spot = Integer.parseInt(spotString);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Enter valid number.");
            return null;
        }
        
        if (name.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Enter name.");
            return null;
        }
        if (address.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Enter MAC address.");
            return null;
        }
        
        String addressRegex = "(([0-9A-Fa-f]{2}[-:]){5}[0-9A-Fa-f]{2})|(([0-9A-Fa-f]{4}\\.){2}[0-9A-Fa-f]{4})";
        if (!address.matches(addressRegex)) {
            JOptionPane.showMessageDialog(rootPane, "MAC address has wrong format.");
        }
        if (numberOfPortsString.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Enter number of ports.");
            return null;
        }
        if (numberOfPorts <= 0) {
            JOptionPane.showMessageDialog(rootPane, "Number of ports must be greater than zero.");
            return null;
        }
        if (spotString.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Enter number of the spot.");
            return null;
        }
        if(spot < 1 || spot > 20) {
            JOptionPane.showMessageDialog(rootPane, "Enter number of the spot.");
            return null;
        }
        
        Device newDevice = new Device.Builder(10L, deviceType, address, numberOfPorts)
                .name(name)
                .build();

        return newDevice;
    }

